    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
 String usuario = rusuario.getText().trim();
        String contrasena = new String(rcontra.getPassword()).trim();

        if (usuario.isEmpty() || contrasena.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Campos vacíos", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (usuarios.containsKey(usuario)) {
            JOptionPane.showMessageDialog(this, "Usuario ya registrado", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Registrar nuevo usuario
        usuarios.put(usuario, contrasena);
        JOptionPane.showMessageDialog(this, "¡Registro exitoso!", "Éxito", JOptionPane.INFORMATION_MESSAGE);

        // Limpiar campos después del registro
        rusuario.setText("");
        rcontra.setText("");

    }//GEN-LAST:event_jButton1ActionPerformed

